// 缓存
const cache = new Map();

/**
 * 提取字符串的「搜索索引」：原文 + 拼音首字母
 * - 中文字符查 PINYIN_TABLE 取首字母
 * - ASCII 字符直通
 * - 标点/空白跳过
 * 多次调用同一字符串走缓存
 */
export function pinyinIndex(str) {
  if (str == null) return '';
  const s = String(str);
  const cached = cache.get(s);
  if (cached) return cached;
  let out = '';
  for (let i = 0; i < s.length; i += 1) {
    const c = s[i];
    const code = c.charCodeAt(0);
    // ASCII 字母数字直通
    if ((code >= 0x30 && code <= 0x39) || (code >= 0x41 && code <= 0x5a) || (code >= 0x61 && code <= 0x7a)) {
      out += c.toLowerCase();
    } else {
      // 汉字查表
      const py = PINYIN_TABLE[c];
      if (py) out += py;
      // 标点/空白/未识别字直接跳过
    }
  }
  cache.set(s, out);
  return out;
}

/**
 * 拼音首字母匹配：原文 OR 拼音首字母 任一匹配关键词
 * @param {string} str - 源文本（如「米饭」）
 * @param {string} kw  - 关键词（如「mf」「米」）
 * @returns {boolean}
 */
export function pinyinMatch(str, kw) {
  if (!str) return false;
  if (!kw) return true;
  const lower = String(str).toLowerCase();
  if (lower.includes(kw)) return true;
  const idx = pinyinIndex(str);
  // 关键词也转拼音首字母再比对（处理同音/形近字，如「西蓝花」→「西兰花」）
  if (idx.includes(kw)) return true;
  const kwIdx = pinyinIndex(kw);
  if (kwIdx && idx.includes(kwIdx)) return true;
  if (kwIdx && lower.includes(kwIdx)) return true;
  return false;
}
